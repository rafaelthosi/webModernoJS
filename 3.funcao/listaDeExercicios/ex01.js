// Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
// multiplicação e divisão desses valores.

function operacoes (x, y) {
    return console.log(`soma: ${x + y}
subtração: ${x - y}
multiplicação: ${x * y}
divisão: ${x / y}`
    )
}

operacoes(5, 4)